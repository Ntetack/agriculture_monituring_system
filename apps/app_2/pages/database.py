import sqlite3
import uuid
import datetime

DB = "agri_monitor.db"

def get_connection():
    conn = sqlite3.connect(DB, check_same_thread=False)
    return conn

def create_table():
    conn = get_connection()
    cursor = conn.cursor()
    cursor.executescript(f"""

        CREATE TABLE IF NOT EXISTS Users (
            userId       INTEGER PRIMARY KEY AUTOINCREMENT,
            userName     TEXT NOT NULL,
            userEmail    TEXT UNIQUE NOT NULL,
            userRole     TEXT NOT NULL,
            passwordHash TEXT NOT NULL,
            lastLogin    DATETIME
        );
        CREATE TABLE IF NOT EXISTS Farms (
            farmId        INTEGER PRIMARY KEY AUTOINCREMENT,
            name          TEXT NOT NULL,
            location      TEXT NOT NULL,
            total_area_ha REAL NOT NULL,
            ownerId       INTEGER NOT NULL REFERENCES Users(userId)
        );
        CREATE TABLE IF NOT EXISTS Fields (
            fieldId     INTEGER PRIMARY KEY AUTOINCREMENT,
            farmId      INTEGER NOT NULL REFERENCES Farms(farmId),
            name        TEXT NOT NULL,
            area_ha     REAL NOT NULL,
            soilType    TEXT NOT NULL,
            gpsBoundary TEXT
        );
        CREATE TABLE IF NOT EXISTS Crops (
            cropId              INTEGER PRIMARY KEY AUTOINCREMENT,
            cropName            TEXT NOT NULL,
            optimalPh_min       REAL,
            optimalPh_max       REAL,
            optimalMoisture_min REAL,
            optimalMoisture_max REAL,
            growthCycleDays     INTEGER
        );
        CREATE TABLE IF NOT EXISTS CropCycles (
            cycleId             INTEGER PRIMARY KEY AUTOINCREMENT,
            fieldId             INTEGER NOT NULL REFERENCES Fields(fieldId),
            cropId              INTEGER NOT NULL REFERENCES Crops(cropId),
            plantingDate        DATE NOT NULL,
            expectedHarvestDate DATE,
            actualHarvestDate   DATE,
            yieldTons           REAL,
            status              TEXT NOT NULL
        );
        CREATE TABLE IF NOT EXISTS IotDevices (
            deviceId           INTEGER PRIMARY KEY AUTOINCREMENT,
            fieldId            INTEGER NOT NULL REFERENCES Fields(fieldId),
            deviceType         TEXT NOT NULL,
            deviceSerialNumber TEXT UNIQUE NOT NULL,
            firmwareVersion    TEXT,
            devicePublicKey    TEXT,
            lastSeen           DATETIME,
            deviceStatus       TEXT NOT NULL
        );
        CREATE TABLE IF NOT EXISTS SensorReadings (
            readingId   INTEGER PRIMARY KEY AUTOINCREMENT,
            deviceId    INTEGER NOT NULL REFERENCES IotDevices(deviceId),
            metricType  TEXT NOT NULL,
            value       REAL NOT NULL,
            unit        TEXT NOT NULL,
            timestamp   DATETIME DEFAULT CURRENT_TIMESTAMP,
            anomalyFlag INTEGER DEFAULT 0
        );
        CREATE TABLE IF NOT EXISTS IrrigationEvents (
            irrigId        INTEGER PRIMARY KEY AUTOINCREMENT,
            fieldId        INTEGER NOT NULL REFERENCES Fields(fieldId),
            irrigStartTime DATETIME NOT NULL,
            irrigEndTime   DATETIME,
            waterVolume_m3 REAL,
            irrigAutomated INTEGER DEFAULT 0
        );
        CREATE TABLE IF NOT EXISTS Alerts (
            alertId   INTEGER PRIMARY KEY AUTOINCREMENT,
            fieldId   INTEGER NOT NULL REFERENCES Fields(fieldId),
            alertType TEXT NOT NULL,
            severity  TEXT NOT NULL,
            message   TEXT NOT NULL,
            createdAt DATETIME DEFAULT CURRENT_TIMESTAMP,
            resolved  INTEGER DEFAULT 0
        );
        CREATE TABLE IF NOT EXISTS WeatherReadings (
            weatherId     INTEGER PRIMARY KEY AUTOINCREMENT,
            farmId        INTEGER NOT NULL REFERENCES Farms(farmId),
            timestamp     DATETIME DEFAULT CURRENT_TIMESTAMP,
            temperature_c REAL,
            humidity_pct  REAL,
            rainfall_mm   REAL,
            windSpeed_kmh REAL
        );
    """)

    conn.commit()

    # INSERT DATA

